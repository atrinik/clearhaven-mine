/** Structure for storing the arena maps */
typedef struct arena_maps_struct {
	/** The arena map path */
	char path[MAX_BUF];

	/** Current number of players in this arena */
	int players;

	/** Maximum number of players for this arena */
	int max_players;

	/** Next arena map */
	struct arena_maps_struct *next;
} arena_map;
